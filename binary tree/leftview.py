#Function to return a list containing elements of left view of the binary tree.
def LeftView(root):
    if root == None:
        return []
    # code here
    q = []
    ans = []
    q.append(root)
    while q:
        n = len(q)
        for i in range(n):
            temp = q[0]
            q.pop(0)
 
            # Print the left most element
            # at the level
            if (i == 0):
                ans.append(temp.data)
 
            # Add left node to queue
            if (temp.left != None):
                q.append(temp.left)
 
            # Add right node to queue
            if (temp.right != None):
                q.append(temp.right)
        
    return ans
        

